def is_fuzzy_match(s1, s2, threshold=FUZZY_MATCH_THRESHOLD):
    """
    Checks if two strings are highly similar using fuzzy logic.
    Useful for matching different declensions of Greek words (e.g., 'Κομοτηνή' vs 'Κομοτηνής').
    """
    return difflib.SequenceMatcher(None, s1.lower(), s2.lower()).ratio() >= threshold

def get_min_dist(coord, memory_list):
    """
    Calculates the minimum distance (in kilometers) between a given coordinate
    and all active fire locations currently stored in the spatiotemporal memory.
    """
    if not memory_list: return float('inf')
    return min(geodesic((coord['lat'], coord['lon']), (m['coords']['lat'], m['coords']['lon'])).km for m in memory_list)

def get_fallback(candidates):
    """
    Returns the candidate location with the highest 'importance' score from the Nominatim API.
    Used as a fallback when spatial context is insufficient for disambiguation.
    """
    return max(candidates, key=lambda x: x['importance'])

def normalize_text(text):
    """
    Removes accents and converts Greek text to lowercase for fair string matching.
    Used for both NER extraction results and Ground Truth comparison.
    """
    if pd.isna(text) or not isinstance(text, str):
        return ""
    text = unicodedata.normalize('NFD', text)
    text = ''.join(char for char in text if unicodedata.category(char) != 'Mn')
    return text.lower().strip()


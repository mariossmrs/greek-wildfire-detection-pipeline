def run_mapping(df, target_label_col='Predicted Label', limit=None, start_date=None, end_date=None):
    """
    Processes the classified dataframe to extract locations, resolve coordinates,
    cluster events, and generate an interactive spatiotemporal map.
    """
    global spatiotemporal_memory
    # Reset spatiotemporal memory for each unique pipeline execution
    spatiotemporal_memory = []

    print("\n--- Step 1: Data Filtering and Preparation ---")

    # Safe data filtering (converts "1", "1.0" and 1 into the same number)
    def safe_filter(dataframe, col_name):

        str_col = dataframe[col_name].astype(str).str.lower().str.strip()

        # Handles potential datatype inconsistencies from upstream modules (e.g., bool, int, string representations of floats)
        valid_fire_labels = ['1', '1.0']
        #valid_fire_labels = ['1', '1.0', 'true', 'fire', 'yes', 'y']

        filtered_df = dataframe[str_col.isin(valid_fire_labels)].copy()

        if filtered_df.empty:
            numeric_col = pd.to_numeric(dataframe[col_name], errors='coerce')
            return dataframe[numeric_col >= 0.5].copy()

        return filtered_df

    if 'Predicted Label' in df.columns:
        print(f"[INFO] Simulation Mode. Values in column 'Predicted Label': {df['Predicted Label'].unique()}")
        fire_df = safe_filter(df, 'Predicted Label')

    elif 'is_fire' in df.columns:
        print(f"[INFO] Standalone Mode. Values in column 'is_fire': {df['is_fire'].unique()}")
        fire_df = safe_filter(df, 'is_fire')

    elif target_label_col in df.columns:
        print(f"[INFO] Fallback Mode. Values in column '{target_label_col}': {df[target_label_col].unique()}")
        fire_df = safe_filter(df, target_label_col)

    else:
        print("[WARNING] Predictions column not found. Continuing with all the data...")
        fire_df = df.copy()

    if limit is not None:
        fire_df = fire_df.head(limit)

    print(f"Total active fire tweets to geoparse: {len(fire_df)}")

    if fire_df.empty:
        print("[ERROR] No fire-related data found to map. Termination process.")
        return None

    # Convert 'Date' column to datetime objects using the specific Twitter/X format
    # This is crucial for the spatiotemporal disambiguation and time-slider functionality
    #fire_df['Date'] = pd.to_datetime(fire_df['Date'], format='%a %b %d %H:%M:%S %z %Y', utc=True, errors='coerce').sort_values()

    # Convert 'Date' column to datetime objects
    fire_df['Date'] = pd.to_datetime(fire_df['Date'], format='%a %b %d %H:%M:%S %z %Y', utc=True, errors='coerce')
    # Sort the entire dataframe chronologically so the spatiotemporal memory works correctly
    fire_df = fire_df.sort_values(by='Date').reset_index(drop=True)

    print("\n--- Step 2: Named Entity Recognition (NER) ---")
    # Extract location entities (GPE, LOC, FAC) using the custom smart extraction function
    fire_df['Extracted_Locations'] = fire_df['Light Cleaned Text'].progress_apply(extract_locations_smart)

    print("\n--- Step 3: Geocoding & Toponym Disambiguation ---")
    # Resolve ambiguous toponyms using spatiotemporal memory and document context
    coords_list = [resolve_coordinates(row) for _, row in tqdm(fire_df.iterrows(), total=len(fire_df), desc="Geocoding")]
    fire_df['Coordinates'] = coords_list

    # Save Geoparsing CSV before Mapping
    fire_df.to_csv('fire_events_geoparsed.csv', index=False)
    print("\n[INFO] Geoparsed dataset securely saved as 'fire_events_geoparsed.csv'")

    print("\n--- Step 4: Clustering & Visualization ---")
    flat_points, meta_info = [], []

    # ---------------------------------------------------------
    # Map Filtering
    # ---------------------------------------------------------
    map_df = fire_df.copy()

    if start_date and end_date:
        try:
            start_dt = pd.to_datetime(start_date, utc=True)
            end_dt = pd.to_datetime(end_date, utc=True) + pd.Timedelta(days=1, seconds=-1)
            mask = (map_df['Date'] >= start_dt) & (map_df['Date'] <= end_dt)
            map_df = map_df.loc[mask]
            print(f"[INFO] Filter applied for mapping: {start_date} to {end_date}.")
            print(f"       Tweets mapped (out of {len(fire_df)} total): {len(map_df)}")
        except Exception as e:
            print(f"[WARNING] Invalid date format in configuration. Mapping all data. Error: {e}")

    if map_df.empty:
        print("[WARNING] No events found in the specified dates. The map will be empty.")

    # 1. Initial collection of map points and RAW predictions
    for _, row in tqdm(map_df.iterrows(), total=len(map_df), desc="Extracting Map Points"):
        if isinstance(row['Coordinates'], list):
            f_pred = row.get('is_wildland_pred', row.get('is_wildland', 0))
            u_pred = row.get('is_urban_pred', row.get('is_urban', 0))

            for c in row['Coordinates']:
                flat_points.append([c['lat'], c['lon']])
                time_str = row['Date'].strftime('%Y-%m-%d %H:%M:%S') if pd.notnull(row.get('Date')) else "Unknown Date"

                # Save raw predictions to meta_info
                meta_info.append({
                    'loc_name': c['name'],
                    'text': row['Light Cleaned Text'],
                    'time': time_str,
                    'f_pred': f_pred,
                    'u_pred': u_pred
                })

    if not flat_points:
        print("No valid coordinates resolved for mapping.")
        return fire_df

    # 2. --- SPATIAL CLUSTERING (DBSCAN) ---
    CLUSTERING_EPSILON_KM = 15.0
    CLUSTERING_MIN_SAMPLES = 2

    # Convert EPSILON from kilometers to radians, as required by scikit-learn's Haversine metric.
    # Earth's mean radius is approximately 6371.0088 km.
    db = DBSCAN(eps=CLUSTERING_EPSILON_KM / 6371.0088, min_samples=CLUSTERING_MIN_SAMPLES, algorithm='ball_tree', metric='haversine')
    labels = db.fit_predict(np.radians(np.array(flat_points)))

    # Tweets per cluster counting
    cluster_counts = Counter(labels)

    # 3. Recognition of "Mixed" Clusters
    cluster_types = {}
    for i, cluster_id in enumerate(labels):
        if cluster_id == -1:
            continue

        if cluster_id not in cluster_types:
            cluster_types[cluster_id] = {'has_forest': False, 'has_urban': False}

        # Conversion to int for safety ('1', 1.0, and 1)
        try:
            f_val = int(float(meta_info[i]['f_pred']))
            u_val = int(float(meta_info[i]['u_pred']))
        except (ValueError, TypeError):
            f_val, u_val = 0, 0

        if f_val == 1:
            cluster_types[cluster_id]['has_forest'] = True
        if u_val == 1:
            cluster_types[cluster_id]['has_urban'] = True

    # 4. Assigning the final 'fire_type'
    for i, meta in enumerate(meta_info):
        cluster_id = labels[i]

        # We store the cluster_id in meta_info to print it in the popup
        meta['cluster_id_debug'] = cluster_id

        try:
            f_val = int(float(meta['f_pred']))
            u_val = int(float(meta['u_pred']))
        except (ValueError, TypeError):
            f_val, u_val = 0, 0

        if cluster_id != -1 and cluster_types[cluster_id]['has_forest'] and cluster_types[cluster_id]['has_urban']:
            meta['fire_type'] = "Mixed Fire (Wildland-Urban Interface)"
        else:
            if f_val == 1 and u_val == 1:
                meta['fire_type'] = "Mixed Fire (Wildland-Urban Interface)"
            elif f_val == 1 and u_val == 0:
                meta['fire_type'] = "Forest Fire"
            elif f_val == 0 and u_val == 1:
                meta['fire_type'] = "Urban Fire"
            else:
                meta['fire_type'] = "Unidentified Type of Fire"

    # --- MAP INITIALIZATION ---
    # Center map on Greece coordinates
    m = folium.Map(location=[MAP_CENTER_LAT, MAP_CENTER_LON], zoom_start=MAP_ZOOM, tiles=None)

    # Add Esri World Imagery (Satellite) as the base layer
    folium.TileLayer(
        tiles='https://server.arcgisonline.com/ArcGIS/rest/services/World_Imagery/MapServer/tile/{z}/{y}/{x}',
        attr='Esri Satellite Imagery',
        name='Esri Satellite'
    ).add_to(m)

    # We also add the simple OSM map
    folium.TileLayer('OpenStreetMap', name='Street View').add_to(m)

    # --- Fire Event Temporal Decay / Fade Out ---

    # Color palette for distinct fire clusters
    colors = ['#FF0000', '#FF8C00', '#FFD700', '#FF4500', '#B22222', '#8B0000', '#E9967A', '#8B4513']

    features = []

    # Fade Out
    opacity_step = INITIAL_OPACITY / FADE_STEPS  # opacity decrease in each step (15%)

    for i, meta in enumerate(meta_info):
        # Convert tweet time to datetime for operations
        try:
            base_time = pd.to_datetime(meta['time'])
        except Exception:
            base_time = pd.to_datetime("2024-01-01 00:00:00") # Security Fallback

        cluster_id = labels[i]
        is_noise = (cluster_id == -1)
        base_color = '#808080' if is_noise else colors[cluster_id % len(colors)]

        if cluster_id == -1:
            size_label = "1"
        else:
            size_label = f"{cluster_counts[cluster_id]}"

        # Creation of multiple "snapshots" (echoes) for the same fire event
        for step in range(FADE_STEPS):
            echo_time = base_time + timedelta(minutes=step * TIME_INTERVAL_MINS)

            # Opacity (brightness) Decrease
            echo_opacity = INITIAL_OPACITY - (step * opacity_step)

            features.append({
                'type': 'Feature',
                'geometry': {'type': 'Point', 'coordinates': [flat_points[i][1], flat_points[i][0]]},
                'properties': {
                    # We set the new display time for the specific echo
                    'time': echo_time.strftime('%Y-%m-%d %H:%M:%S'),
                    # Popup display remains stable, showing the real fire time (meta['time'])
                    'popup': f"<b>Location:</b> <b>{meta['loc_name']}</b> (Cluster: {meta['cluster_id_debug']})<br><b>Timestamp:</b> {meta['time']}<br><b>Type:</b> {meta['fire_type']}<br><b>Tweet:</b> <i>{meta['text']}</i><br><br>This cluster has <b>{size_label}</b> total incident reports<br>",
                    'icon': 'circle',
                    'iconstyle': {
                        'fillColor': base_color,
                        'fillOpacity': echo_opacity,
                        'radius': 8,
                        'stroke': False
                    }
                }
            })

    # --- TIME SLIDER INTEGRATION ---
    # Adds a dynamic time slider to visualize the progression of fire reports
    TimestampedGeoJson(
        {'type': 'FeatureCollection', 'features': features},
        period='PT30M', # 30-minute steps
        duration='PT30M', # Events persist for 30-min hours on map
        time_slider_drag_update=True
    ).add_to(m)

    # Map layer control
    folium.LayerControl(position='topright').add_to(m)

    # Save MAP locally in the Colab environment
    m.save('fire_events_map.html')

    return fire_df


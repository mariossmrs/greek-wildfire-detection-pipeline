def evaluate_geoparsing(eval_df, distance_threshold_km=EVALUATION_DISTANCE_KM):
    """
    Calculates NER extraction accuracy and Geocoding spatial accuracy.
    Assumes the input dataframe is the flattened version containing both
    predictions and ground truth.
    """
    print("\n--- STARTING EVALUATION ---")

    total_targets = len(eval_df)
    if total_targets == 0:
        print("[WARNING] No data available for evaluation.")
        return eval_df

    # 1. NER Evaluation (Strict & Partial Matching)
    ner_strict_correct = 0
    ner_partial_correct = 0

    for _, row in eval_df.iterrows():
        extracted = normalize_text(row.get('NER_Extracted', ''))
        actual = normalize_text(row.get('Actual_Toponym', ''))

        if extracted == 'none' and actual == '':
            # True Negative: Model correctly identified no location
            ner_strict_correct += 1
            ner_partial_correct += 1
        elif extracted != '' and actual != '':
            if extracted == actual:
                # Exact match
                ner_strict_correct += 1
                ner_partial_correct += 1
            elif extracted in actual or actual in extracted:
                # Partial match (e.g., 'Αθήνα' in 'Δήμος Αθηναίων')
                ner_partial_correct += 1

    ner_strict_acc = (ner_strict_correct / total_targets) * 100 if total_targets > 0 else 0
    ner_partial_acc = (ner_partial_correct / total_targets) * 100 if total_targets > 0 else 0

    print(f"\n[1] NER PERFORMANCE")
    print(f"Total Entities Evaluated     : {total_targets}")
    print(f"--------------------------------------------------")
    print(f"Strictly Correct Entities    : {ner_strict_correct}")
    print(f"Strict Accuracy              : {ner_strict_acc:.2f}%")
    print(f"--------------------------------------------------")
    print(f"Partially Correct Entities   : {ner_partial_correct}")
    print(f"Partial/Operational Accuracy : {ner_partial_acc:.2f}%") # Indicates how often the model correctly identified the geographic entity
    print(f"--------------------------------------------------")

    # 2. Geocoding Evaluation (Spatial Accuracy)
    distances = []
    correct_within_threshold = 0
    valid_geo_targets = 0

    for _, row in eval_df.iterrows():
        # Only evaluate geocoding if there is an actual location in Ground Truth
        if pd.notna(row.get('Actual_Lat')) and str(row.get('Actual_Lat')).strip() != '':
            valid_geo_targets += 1

            pred_lat = row.get('Predicted_Lat')
            pred_lon = row.get('Predicted_Lon')
            act_lat = row.get('Actual_Lat')
            act_lon = row.get('Actual_Lon')

            # --- Calculation only if coordinates exist ---
            if pd.notna(pred_lat) and pd.notna(pred_lon):
                try:
                    dist = geodesic((float(pred_lat), float(pred_lon)), (float(act_lat), float(act_lon))).km

                    distances.append(dist)
                    if dist <= distance_threshold_km:
                        correct_within_threshold += 1

                except (ValueError, TypeError) as e:
                    print(f"[WARNING] Could not calculate distance for Tweet_ID {row.get('Tweet_ID')}: {e}")
                    continue

    if valid_geo_targets > 0:
        geo_accuracy = (correct_within_threshold / valid_geo_targets) * 100

        mean_error = np.nanmean(distances) if distances else 0
        median_error = np.nanmedian(distances) if distances else 0

        print(f"\n[2] GEOCODING PERFORMANCE")
        print(f"Total Valid Target Coordinates : {valid_geo_targets}")
        print(f"Geocoded within {distance_threshold_km}km       : {correct_within_threshold}")
        print(f"Operational Accuracy           : {geo_accuracy:.2f}%") # Indicates the percentage of cases where the system can direct emergency services to the correct broader area
        print(f"Mean Distance Error            : {mean_error:.2f} km")
        print(f"Median Distance Error          : {median_error:.2f} km")
    else:
        print(f"\n[2] GEOCODING PERFORMANCE: No valid coordinates to evaluate.")

    print("==================================================\n")

    return eval_df

@lru_cache(maxsize=1024)
def get_candidates_cached(loc_name):
    """
    Fetches geographic coordinates from the Nominatim API for a given location name.
    Implements API rate-limiting, caching (to avoid redundant calls), and 'Highway Removal'
    (ignoring streets/roads) to focus on wider geographic areas relevant to wildfires.
    """
    try:
        time.sleep(1.1) # Respect Nominatim API usage policy (1 request/sec)
        results = geolocator.geocode(loc_name, country_codes=['gr', 'cy'], exactly_one=False, limit=15, timeout=10) # We add Cyprus

        if not results: return []

        valid_candidates = []
        for res in results:
            raw = res.raw
            # Heuristic: Ignore streets/highways as wildfires usually cover broader areas
            if raw.get('class') == 'highway': continue

            # Heuristic: Ignore Embassies & Consulates
            if raw.get('type') in ['embassy', 'consulate']: continue

            valid_candidates.append({
                'lat': res.latitude, 'lon': res.longitude,
                'name': res.address.split(',')[0],
                'osm_class': raw.get('class', 'unknown'),
                'osm_type': raw.get('type', 'unknown'),
                'importance': raw.get('importance', 0.0)
            })
        return valid_candidates
    except Exception as e:
        print(f"  [API Error] '{loc_name}': {e}")
        time.sleep(2)
        return []

def extract_locations_smart(text):
    """
    Extracts Location Entities (GPE, LOC, FAC) using the GR-NLP toolkit.
    Includes hashtag symbol removal (to prevent NER blindness), macro-location filtering,
    and intra-tweet fuzzy deduplication (merging variations of the same name within one tweet).
    """
    if not isinstance(text, str): return []

    # Remove hashtags before NER processing (to be deleted after cleaning update)
    doc = nlp_pipeline(text.replace('#', ''))

    raw_locs = {}
    curr_loc, curr_tag = [], ""

    # Append None to ensure the buffer flushes on the final iteration
    for token in list(doc.tokens) + [None]:
        if token and token.ner and token.ner.endswith(("LOC", "GPE", "FAC")):
            curr_loc.append(token.text)
            curr_tag = token.ner.split('-')[-1]
        elif curr_loc:
            loc_str = " ".join(curr_loc).strip()
            if len(loc_str) > 2 and loc_str.lower() not in MACRO_LOCATIONS:
                raw_locs[loc_str] = curr_tag
            curr_loc, curr_tag = [], ""

    # Intra-tweet Fuzzy Deduplication (e.g., merge 'Αθήνα' and 'Αθήνας' if found in the same tweet)
    unique_locs = []
    for name, tag in raw_locs.items():
        if not any(is_fuzzy_match(name, u['name']) for u in unique_locs):
            unique_locs.append({'name': name, 'ner_label': tag})

    return unique_locs

def resolve_coordinates(row):
    global spatiotemporal_memory
    loc_list, tweet_time = row['Extracted_Locations'], row['Date']

    if not loc_list or pd.isnull(tweet_time): return []

    spatiotemporal_memory = [m for m in spatiotemporal_memory if (tweet_time - m['time']).total_seconds() <= TIME_WINDOW_HOURS * 3600]
    final_coords, unresolved_locs = [], []

    # 2. Pre-check: Memory location
    for loc in loc_list:
        match = next((m for m in spatiotemporal_memory if is_fuzzy_match(loc['name'], m['loc_name'])), None)
        if match:
            c_copy = match['coords'].copy()
            c_copy['ner_label'] = loc['ner_label']
            c_copy['original_name'] = loc['name']
            final_coords.append(c_copy)
            match['time'] = tweet_time
        else:
            unresolved_locs.append(loc)

    if not unresolved_locs: return final_coords

    cands_dict = {loc['name']: get_candidates_cached(loc['name']) for loc in unresolved_locs if get_candidates_cached(loc['name'])}
    ner_dict = {loc['name']: loc['ner_label'] for loc in unresolved_locs}
    locs_found = list(cands_dict.keys())

    if not locs_found: return final_coords

    # CASE A: Single location found
    if len(locs_found) == 1:
        loc_name = locs_found[0]
        cands = cands_dict[loc_name]

        best_cand = min(cands, key=lambda c: get_min_dist(c, spatiotemporal_memory), default=None)
        min_dist = get_min_dist(best_cand, spatiotemporal_memory) if best_cand else float('inf')

        is_valid = best_cand and ((cands.index(best_cand) == 0 and min_dist < DISAMBIGUATION_THRESHOLD_KM) or (cands.index(best_cand) > 0 and min_dist < STRICT_THRESHOLD_KM))

        chosen_coord = best_cand.copy() if is_valid else get_fallback(cands).copy()
        chosen_coord['ner_label'] = ner_dict[loc_name]
        chosen_coord['original_name'] = loc_name

        final_coords.append(chosen_coord)
        spatiotemporal_memory.append({'loc_name': loc_name, 'coords': chosen_coord, 'time': tweet_time})

    # CASE B: Multiple locations found
    else:
        resolved_dict = {}
        if spatiotemporal_memory:
            for loc in locs_found:
                best_cand = min(cands_dict[loc], key=lambda c: get_min_dist(c, spatiotemporal_memory), default=None)
                if best_cand and get_min_dist(best_cand, spatiotemporal_memory) < DISAMBIGUATION_THRESHOLD_KM:
                    c_copy = best_cand.copy()
                    c_copy['ner_label'] = ner_dict[loc]
                    c_copy['original_name'] = loc
                    resolved_dict[loc] = c_copy

        if resolved_dict:
            final_coords.extend(resolved_dict.values())
            anchors = list(resolved_dict.values())
        else:
            best_pair = None
            min_dist = float('inf')
            # If multiple unknown locations are extracted, we compute the distance between all pairs
            # to find the tightest spatial cluster, assuming they refer to the same incident.
            for l1, l2 in combinations(locs_found, 2):
                for c1 in cands_dict[l1]:
                    for c2 in cands_dict[l2]:
                        dist = geodesic((c1['lat'], c1['lon']), (c2['lat'], c2['lon'])).km
                        if dist < min_dist:
                            min_dist, best_pair = dist, {l1: c1, l2: c2}

            if best_pair and min_dist < DISAMBIGUATION_THRESHOLD_KM:
                for l_name, c in best_pair.items():
                    c_copy = c.copy()
                    c_copy['ner_label'] = ner_dict[l_name]
                    c_copy['original_name'] = l_name
                    final_coords.append(c_copy)
                    spatiotemporal_memory.append({'loc_name': l_name, 'coords': c_copy, 'time': tweet_time})
                anchors = list(final_coords[-len(best_pair):])
            else:
                for loc in locs_found:
                    best_c = get_fallback(cands_dict[loc]).copy()
                    best_c['ner_label'] = ner_dict[loc]
                    best_c['original_name'] = loc
                    final_coords.append(best_c)
                    spatiotemporal_memory.append({'loc_name': loc, 'coords': best_c, 'time': tweet_time})
                return final_coords

        centroid = (np.mean([c['lat'] for c in anchors]), np.mean([c['lon'] for c in anchors]))
        for loc in locs_found:
            if loc not in [a.get('original_name') for a in anchors]:
                best_c = min(cands_dict[loc], key=lambda c: geodesic((c['lat'], c['lon']), centroid).km).copy()
                if geodesic((best_c['lat'], best_c['lon']), centroid).km < DISAMBIGUATION_THRESHOLD_KM:
                    best_c['ner_label'] = ner_dict[loc]
                    best_c['original_name'] = loc
                    final_coords.append(best_c)
                    spatiotemporal_memory.append({'loc_name': loc, 'coords': best_c, 'time': tweet_time})

    return final_coords


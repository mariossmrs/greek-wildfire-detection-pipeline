def align_geoparsed_results(geoparsed_df, ground_truth_df):
    """
    Synchronizes predictions with Ground Truth dataset using only the Tweet_ID.
    """
    flattened_data = []

    # Cleaning
    geoparsed_df['Tweet_ID_clean'] = geoparsed_df['Tweet_ID'].astype(str).str.replace(r'\.0$', '', regex=True).str.strip()
    ground_truth_df['Tweet_ID_clean'] = ground_truth_df['Tweet_ID'].astype(str).str.replace(r'\.0$', '', regex=True).str.strip()

    for _, gt_row in ground_truth_df.iterrows():
        t_id = gt_row['Tweet_ID_clean']
        gt_toponym = normalize_text(gt_row.get('Actual_Toponym', ''))

        base_row = {
            'Tweet_ID': gt_row['Tweet_ID'],
            'Raw_Text': gt_row.get('Raw_Text', ''),
            'Actual_Toponym': gt_row.get('Actual_Toponym', ''),
            'Actual_Lat': gt_row.get('Actual_Lat', None),
            'Actual_Lon': gt_row.get('Actual_Lon', None),
            'Notes': gt_row.get('Notes', ''),
            'NER_Extracted': 'MISSING',
            'Geocoded_As': 'NONE',
            'Predicted_Lat': None,
            'Predicted_Lon': None
        }

        # We find the corresponding tweet in the geoparsing results
        pred_match = geoparsed_df[geoparsed_df['Tweet_ID_clean'] == t_id]

        if not pred_match.empty:
            p_row = pred_match.iloc[0]
            ner_locs = p_row.get('Extracted_Locations', [])
            geo_coords = p_row.get('Coordinates', [])

            if isinstance(ner_locs, list):
                for loc in ner_locs:
                    pred_toponym = normalize_text(loc.get('name', ''))

                    if pred_toponym != "" and gt_toponym != "" and (pred_toponym in gt_toponym or gt_toponym in pred_toponym):
                        base_row['NER_Extracted'] = loc.get('name', '')

                        if isinstance(geo_coords, list):
                            matching_coord = next((c for c in geo_coords if c.get('original_name') == loc.get('name')), None)

                            if matching_coord:
                                base_row['Geocoded_As'] = matching_coord.get('name', '')
                                base_row['Predicted_Lat'] = matching_coord.get('lat', None)
                                base_row['Predicted_Lon'] = matching_coord.get('lon', None)

                        break

        flattened_data.append(base_row)

    return pd.DataFrame(flattened_data)

